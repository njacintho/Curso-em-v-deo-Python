le = input('Digite algo: ')
print('O tipo primitivo desse valor é {}'.format(type(le)))
print('Só tem espços? {}' .format(le.isspace()))
print('É um número? {}'.format(le.isnumeric()))
print('É alfabético? {}'.format(le.isalpha()))
print('É alfanumérico {}'.format(le.isalnum()))
print('Está em maiúsculas? {}'.format(le.isupper()))
print('Está em minúsculas? {}'.format(le.islower()))
print('Está capitalizada {}'.format(le.istitle())) #se a primeira letra está em maiuscula




